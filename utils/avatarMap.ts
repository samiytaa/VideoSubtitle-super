export const normalizeAvatarName = (avatarName: string): string => {
  if (!avatarName) return '';
  return avatarName.startsWith(LEGACY_AVATAR_PREFIX)
    ? avatarName.slice(LEGACY_AVATAR_PREFIX.length)
    : avatarName;
};

export const resolveAvatarName = (avatarName: string): string | null => {
  if (!avatarName) return null;

  const normalizedName = normalizeAvatarName(avatarName);
  if (avatarMap[normalizedName]) return normalizedName;
  if (avatarMap[avatarName]) return avatarName;
  return null;
};

/**
 * 获取头像的完整路径
 * @param avatarName 头像名称
 * @returns 完整的图片路径，如果未找到则返回 null
 */
export const getAvatarPath = (avatarName: string): string | null => {
  if (!avatarName) return null;

  const resolvedAvatarName = resolveAvatarName(avatarName);
  const relativePath = resolvedAvatarName ? avatarMap[resolvedAvatarName] : undefined;
  if (!relativePath) {
    logger.warn(`未找到头像映射: ${avatarName}`);
    return null;
  }

  const normalizedPath = `${LOCAL_AVATAR_ROOT}/${relativePath}`;
  return `${import.meta.env.BASE_URL}${encodeURI(normalizedPath)}`;
};

/**
 * 获取所有可用的头像名称列表
 * @returns 头像名称数组
 */
export const getAvailableAvatars = (): string[] => {
  return Object.keys(avatarMap);
};

const normalizeAvatarLookupText = (value: string): string => {
  return normalizeAvatarName(value)
    .trim()
    .replace(/[·•・]/g, '')
    .replace(/\s+/g, '')
    .replace(/[()（）\[\]【】]/g, '');
};

const buildAvatarMatchScore = (characterName: string, avatarName: string, avatarPath: string): number => {
  const normalizedCharacter = normalizeAvatarLookupText(characterName);
  const normalizedAvatar = normalizeAvatarLookupText(avatarName);

  if (!normalizedCharacter || !normalizedAvatar.includes(normalizedCharacter)) {
    return Number.NEGATIVE_INFINITY;
  }

  let score = 0;

  if (avatarName === characterName) score += 2000;
  if (avatarName.startsWith(`${characterName}-`)) score += 1500;
  if (avatarName.endsWith(`·${characterName}`)) score += 500;
  if (normalizedAvatar === normalizedCharacter) score += 1200;

  if (avatarPath.includes('小头像-')) score += 300;
  if (avatarName.includes('默认')) score += 120;
  if (avatarName.includes('正常')) score += 120;
  if (avatarName.includes('微笑')) score += 80;

  if (avatarName.includes('q版') || avatarName.includes('QQ人')) score -= 220;
  if (avatarName.includes('离魂')) score -= 260;
  if (avatarName.includes('心纸君')) score -= 180;
  if (avatarName.includes('猫绒绒') || avatarName.includes('狗绒绒')) score -= 160;

  score -= avatarName.length * 0.5;
  return score;
};

/**
 * 根据角色名自动查找最合适的头像
 * 优先普通小头像，其次默认/常规表情头像，再退化到其他变体
 */
export const findBestAvatarMatch = (characterName: string): string | null => {
  if (!characterName?.trim()) return null;

  let bestAvatar: string | null = null;
  let bestScore = Number.NEGATIVE_INFINITY;

  for (const [avatarName, avatarPath] of Object.entries(avatarMap)) {
    const score = buildAvatarMatchScore(characterName, avatarName, avatarPath);
    if (score > bestScore) {
      bestScore = score;
      bestAvatar = avatarName;
    }
  }

  return Number.isFinite(bestScore) ? bestAvatar : null;
};
